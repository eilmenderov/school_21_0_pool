int	ft_is_prime(int nb)
{
	int del;

	if (nb < 2)
		return (0);
	del = 2;
	while (del != nb && (nb / del) + 1 > del)
	{
		if (nb % del == 0)
			return (0);
		del++;
	}
	return (1);
}
