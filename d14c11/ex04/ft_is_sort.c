int	ft_is_sort(int *tab, int length, int (*f) (int, int))
{
	int i;
	int a;
	int b;

	a = 0;
	b = 0;
	i = 0;
	while (i < length - 1)
	{
		if (f(tab[i], tab[i + 1]) > 0)
			b++;
		if (f(tab[i], tab[i + 1]) < 0)
			a++;
		i++;
	}
	if ((b == 0 && a > 0) || (b > 0 && a == 0) || (a == 0 && b == 0))
		return (1);
	return (0);
}
